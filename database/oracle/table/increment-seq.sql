DECLARE
	V_COUNT NUMBER;
BEGIN
	SELECT count(1) INTO V_COUNT from USER_SEQUENCES where SEQUENCE_NAME = 'TICKET_DETAIL_SEQ';
	IF V_COUNT = 0 THEN
		EXECUTE IMMEDIATE ('CREATE SEQUENCE TICKET_DETAIL_SEQ
    		INCREMENT BY 1
  		START WITH 1200000000000000 
 		MINVALUE 1200000000000000 
 		MAXVALUE 1299999999999999
 		NOCACHE ');
		DBMS_OUTPUT.PUT_LINE('SECUENCIA CREADA SATISFACTORIAMENTE: TICKET_DETAIL_SEQ');
	ELSE 
		DBMS_OUTPUT.PUT_LINE('SECUENCIA VALIDADA: TICKET_DETAIL_SEQ' );
	END IF;	
		
END;